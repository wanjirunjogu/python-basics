# Python Collections (Arrays)
# There are four collection data types in the Python programming language:

# * List  - is a collection which is ordered and changeable. Allows duplicate members.
# * Tuple - is a collection which is ordered and unchangeable. Allows duplicate members.
# * Set   - is a collection which is unordered and unindexed. No duplicate members.
# * Dictionary -is a collection which is unordered, changeable and indexed. No duplicate members.

# When choosing a collection type, it is useful to understand the properties of that type.
# Choosing the right type for a particular data set could mean retention of meaning, 
# and, it could mean an increase in efficiency or security.



#This is a sample of a List
cars = ["Bugati", "Ferari", "BMW", "Merc", "Royce Rolls"]
print(cars)

#This is a sample of  tuple 
family = ("Liam", "Vero", "Hannah", "Benard")
print(family)

# This is a sample of  Set
person = {"name" : "Mary Wanjiru" , "gender" : "Female" , "school" : "Kibabii"}
print(person)

# This is a sample of  Dictionary
names = {"Mary", "Wanjiru", "Njogu"}
print(names)