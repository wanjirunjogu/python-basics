# Text Type:	str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool

# String data type
y = "Wanjiru Njogu"
print(type(y))

# Int data type
x = 10
print(type(x))

# float data type
t = 30.02
print(type(t))

# List data type
z = ["Mary","Wanjiru","Njogu"]
print(type(z)) 

# Tuple data type 
p = ("Mary","Vero","Liam")
print(type(p))

# These are range types
c = range(10)
print(type(c))

# Dictionary types
h = {"name" : "Wanjiru Mary", "Age" : 20}
print(type(h))

# Set data type
g = {"Liam", "Macharia", "Njogu"}
print(type(g))

# Frozenset data type
u = frozenset({"Liam", "Macharia", "Njogu"})
print(type(u))


# bool boolean datatype
e = True
print(type(e))
